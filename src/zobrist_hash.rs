// first castling index
pub const CASTLING_INDEX_START: usize = 768; 

// index right before Enpassant index
pub const ENPASSANT_INDEX_START: usize = 783;

pub const BOARD_COLOR_HASH: u64 = 0xd1139fcf116190b;


// this only for pieces
pub fn get_zobrist_piece_index(piece_type: u8, square: u8) -> usize{
    return ((piece_type as u16) * 64 + (square as u16)) as usize;
}

pub fn get_full_zobrist_hash(chess_board: &ChessBoard) -> u64{
    let mut zobrist_hash : u64 = 0;
    
    for i in 0..12{
        let mut temp_bitboard = chess_board.piece_bitboards[i as usize];

        while temp_bitboard != 0{
            let square : u8 = temp_bitboard.trailing_zeros() as u8;

            zobrist_hash ^= zobrist_hash_table[get_zobrist_piece_index(i, square)];

            temp_bitboard ^= 1 << square;
        }
    }

    // color hash
    if !chess_board.board_color{
        zobrist_hash ^= BOARD_COLOR_HASH;
    }

    // gets the castling information
    let castling_info : usize = (chess_board.board_info & 0xF) as usize;

    zobrist_hash ^= zobrist_hash_table[CASTLING_INDEX_START + castling_info];

    // gets the enpassant information
    let enpassant_info : usize = ((chess_board.board_info >> 4) & 0xF) as usize;

    if enpassant_info != 0{
        zobrist_hash ^= zobrist_hash_table[ENPASSANT_INDEX_START + enpassant_info];
    }

    return zobrist_hash;
}